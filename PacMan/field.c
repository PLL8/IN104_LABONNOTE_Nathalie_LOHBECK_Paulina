void affichage(int** area)
{
	for (int i=0; i<H; i++)
	{
		for (int j=0; j<W; j++)
		{
			if(area[i][j]==0)
			{
				printf(".");
			} else if (area[i][j]==-1) 
			{
				printf(" ");
			}
			else if (area[i][j]==1)
			{
				printf("#");
			} else if (area[i][j]==2)
			{
				printf("P");
			} else if (area[i][j]==3)
			{
				printf("G");
			}	
		}
		printf("\n");
	}
}



